$a = 1; 
$b = -3; 
$c = 2;

$d = $b * $b - 4 * $a * $c;

echo "Уравнение: {$a}x² + ({$b})x + ({$c}) = 0 <br>";
echo "Дискриминант D = $d <br>";


if ($d > 0) {
    $x1 = (-$b + sqrt($d)) / (2 * $a);
    $x2 = (-$b - sqrt($d)) / (2 * $a);
    echo "Результат: x1 = $x1, x2 = $x2";
} elseif ($d == 0) {
    $x = -$b / (2 * $a);
    echo "Результат: x = $x";
} else {
    echo "Результат: Нет корней";
}
